const CATEGORY_ICONS: Record<string, string> = {
    'Recent': '🕐',
    'Smileys': '😊',
    'Gestures': '👋',
    'Hearts': '❤️',
    'Animals': '🐶',
    'Food': '🍔',
    'Objects': '💡',
    'Symbols': '💯',
};

export default function EmojiKeyboard({ onSelect, onClose }: EmojiKeyboardProps) {
    const [selectedCategory, setSelectedCategory] = useState('Recent');
    const categories = Object.keys(EMOJI_CATEGORIES);

    const renderEmoji = ({ item }: { item: string }) => (
        <Pressable
            onPress={() => onSelect(item)}
            className="w-10 h-10 items-center justify-center"
        >
            <Text className="text-2xl">{item}</Text>
        </Pressable>
    );

    return (
        <View className="bg-zinc-800 rounded-t-2xl overflow-hidden">
            {/* Category tabs */}
            <ScrollView
                horizontal
                showsHorizontalScrollIndicator={false}
                className="border-b border-zinc-700"
                contentContainerStyle={{ paddingHorizontal: 8 }}
            >
                {categories.map(category => (
                    <Pressable
                        key={category}
                        onPress={() => setSelectedCategory(category)}
                        className={`px-3 py-2 mx-1 rounded-lg ${selectedCategory === category ? 'bg-zinc-700' : ''}`}
                    >
                        <Text className="text-xl">{CATEGORY_ICONS[category] || '📁'}</Text>
                    </Pressable>
                ))}
            </ScrollView>

            {/* Emoji grid */}
            <FlatList
                data={EMOJI_CATEGORIES[selectedCategory as keyof typeof EMOJI_CATEGORIES]}
                renderItem={renderEmoji}
                keyExtractor={(item, index) => `${item}-${index}`}
                numColumns={8}
                contentContainerStyle={{ padding: 8 }}
                style={{ maxHeight: 200 }}
            />
        </View>
    );
}
